int main(void)
{
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_TIM1_Init();
    MX_USART2_UART_Init();

    morse_init();

    char message[128];
    char morse[512];

    while (1)
    {
        printf("\r\n========================================\r\n");
        printf("[ATTENTE] Entrez un nouveau message :\r\n");

        lecture_message(message, sizeof(message));

        printf("[OK] Message recu : \"%s\"\r\n", message);
        printf("[OK] Longueur : %u caractere(s)\r\n", (unsigned)strlen(message));

        text_to_morse(message, morse, sizeof(morse));
        printf("[OK] Morse : %s\r\n", morse);

        printf("[TX] Debut emission Morse sur le buzzer...\r\n");
        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);

        buzzer_morse(morse);

        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
        printf("[OK] Transmission Morse terminee.\r\n");
    }
}

